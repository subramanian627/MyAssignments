package week1.day3;

public class CheckNumberIsPositive {
	public static void main(String[] args) {
		
		int a = -10;
		if (a > 0) {
			System.out.println("The value is positive number");	
		} else if (a < 0) {
			System.out.println("The value is negative number");
		} else {
			System.out.println("The value is zero");
		}
		
	}

}
